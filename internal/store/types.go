package store

import "database/sql"

// State represents a message's lifecycle position in the queue.
type State string

const (
	StateQueued       State = "queued"
	StateDelivering   State = "delivering"
	StateDelivered    State = "delivered"
	StateFailed       State = "failed"
	StateAcknowledged State = "acknowledged" // #221: operator-acked announce-skipped backlog residue
	// StateDeferred is a pre-`queued` holding state (#227): the row exists
	// but is not yet eligible for mailman pickup. It carries a non-empty
	// deliver_after trigger and is promoted to `queued` when that trigger
	// fires (a flush_deferred call). Deferred rows are invisible to
	// ClaimNext / inbox / mailman until promoted — the deferral lives in
	// `state`, not in delivery-cadence policy, so the mailman stays eager
	// and the single-writer invariant is untouched.
	StateDeferred State = "deferred"
)

// Kind distinguishes a paste-rendered chat message from a control command
// that is typed directly into the recipient pane.
type Kind string

const (
	KindMessage Kind = "message"
	KindControl Kind = "control"
	// KindDeliveryFailureNotice marks a message auto-generated by the
	// mailman when one of the recipient's outbound messages transitioned
	// to a terminal-failure state. The kind is the load-bearing
	// loop-prevention marker (per #53): a notice that itself fails to
	// deliver MUST NOT generate another notice — otherwise a wedged
	// pane on either side would burn the message queue. The mailman's
	// failure-hook checks the failed message's kind before generating
	// and silently skips notices.
	KindDeliveryFailureNotice Kind = "delivery_failure_notice"
	// KindStrandedDraft marks a message auto-generated by the mailman
	// when the observe-gate (#92) flushes operator-typed content out of
	// the input row to make room for delivery. The cleared content is
	// snapshotted as a stranded_draft row addressed back to the agent
	// so the operator can recover their typed work after the fact. Per
	// the (c) Clear-paste-archive flush semantics agreed at #92's
	// 2026-06-04 design comment: explicitly preferred over (b)
	// Clear-and-discard because the input row's content may itself be a
	// half-delivered bus message from a previous failed delivery (paste
	// succeeded but verify-token never landed); blind Ctrl+U would
	// destroy bus content not operator content.
	KindStrandedDraft Kind = "stranded_draft"
	// KindPing marks a substrate-only reachability probe (#144). The
	// mailman picks the row up like any other message — which itself
	// proves the daemon is alive — but instead of paste-and-Enter it
	// runs substrate-health checks (recipient registered, pane live)
	// and transitions the row straight to delivered (healthy) or
	// failed (unreachable). The whole point is to exercise the
	// DB-insert → mailman-pickup → delivery-state path WITHOUT pasting
	// into the recipient's pane: a "is the daemon up + agent reachable?"
	// probe that loads no recipient context.
	KindPing Kind = "ping"
	// KindDedupeNotice marks a message auto-generated by the mailman when
	// the dedupe path absorbs a replay (#157 PR2): the original
	// delivered_in_input_box message was re-verified as confirmed and the
	// replay was dropped. The notice travels back to the original sender
	// as a positive resolution signal — distinct from KindDeliveryFailureNotice
	// which signals a failure.
	KindDedupeNotice Kind = "dedupe_notice"
	// KindBacklogAnnounce marks the synthetic "📬 N queued — run inbox"
	// nudge the register handler inserts when an agent (re)registers with a
	// queued backlog (#204). Like the other synthetic kinds it rides the
	// normal mailman loop (single-writer-safe — the register process never
	// pastes), but its body is a one-shot heads-up, not bus content: it tells
	// a freshly-restarted session it has mail waiting without the mailman
	// flooding the pane with the whole pre-existing backlog at once.
	KindBacklogAnnounce Kind = "backlog_announce"
)

// Message mirrors a row in the messages table. Timestamps are kept as
// strings (ISO 8601 UTC) — callers parse to time.Time at presentation time
// if needed; the store layer is timezone-agnostic.
type Message struct {
	ID              int64
	PublicID        string
	FromAgent       string
	ToAgent         string
	ReplyTo         sql.NullString
	Body            string
	Kind            Kind
	NoReplyExpected bool
	Quick           bool // compact single-line chrome (#154)
	State           State
	CreatedAt       string
	DeliveredAt     sql.NullString
	Error           sql.NullString
	// Replay linkage (#157 PR1), set only on messages created by `resend`.
	// ReplayOf is the original message's public_id; ReplayOfAt is the
	// original's created_at (ISO 8601 UTC) — carried on the row so the pure
	// render layer can show the "Replayed: original sent at <ts>" marker
	// without a store lookup. Both invalid/empty for normal messages.
	ReplayOf   sql.NullString
	ReplayOfAt sql.NullString
	// DeliverAfter mirrors the #227 `deliver_after` column: the trigger that
	// promotes a deferred row to `queued`. Non-empty only on rows in
	// StateDeferred (the trigger name, e.g. "resume"); NULL/invalid for normal
	// messages. Read for visibility (`sent --deferred`); the promotion itself
	// matches on the column via SQL, not this field.
	DeliverAfter sql.NullString
	// Verified mirrors the #169 `verified` column. 1 = verify-token observed
	// (confirmed delivery), 0 = delivered_in_input_box soft-fail (paste landed,
	// token never surfaced), NULL/invalid = pre-migration row or not yet
	// delivered. Orthogonal to State: both verified and unverified rows carry
	// State == StateDelivered.
	Verified sql.NullInt64
	// ExpectsReply mirrors the #250 `expects_reply` column. true = sender
	// flagged intent to receive a reply (set by `ask` or `send --expects-reply`).
	// false (default) = normal send. Read by inbox --unanswered / sent
	// --awaiting-reply filters (#270).
	ExpectsReply bool
	// Priority mirrors the #449 `priority` column — the integer weight
	// (low=10 / normal=20 / high=30) that the cross-channel scheduler uses.
	// Default PriorityNormal (20). Render with PriorityName.
	Priority int
	// ForceRateLimited mirrors the #558 `force_rate_limited` column — the
	// operator escape-hatch set by `send --force-rate-limited`. true → the
	// mailman bypasses the rate-limit / usage-limit defer gates for this message
	// (but NOT copy-mode / awaiting-operator / unknown / compaction paste-unsafety).
	// Populated only on the ClaimNextWithStrategy delivery read path (the sole
	// consumer); other message-row reads leave it false. Default false.
	ForceRateLimited bool
}

// Agent mirrors a row in the agents table.
type Agent struct {
	Name      string
	PaneID    string // empty when pane_id is NULL
	Paused    bool
	UpdatedAt string
	// Aliases is the JSON-decoded list of alternative names the
	// discover walker should accept for this canonical agent. Empty
	// list when no aliases registered. See #38.
	Aliases []string
	// DeliveryMode controls how the mailman delivers to this agent
	// (#116). Two values:
	//   - "paste-and-enter" (default): mailman delivers via tmux paste
	//     + Enter; the existing behavior for CLI-tool-hosting panes.
	//   - "mailbox-only": mailman does NOT paste; messages stay in
	//     state=queued and the operator polls via `claude-msg inbox`.
	//     The operator-as-bus-participant scenario from ADR-0005
	//     §Decision (1)'s wheel-reinvention check.
	// Default for newly-inserted agents (and the back-compat ALTER's
	// DEFAULT) is "paste-and-enter".
	DeliveryMode string
	// BacklogEpoch is the #204 claim-floor: the highest message id that
	// counts as pre-existing backlog at the agent's last (re)register.
	// The mailman's ClaimNext skips queued rows with id <= this value, so
	// a freshly-restarted session is not flooded with its whole backlog.
	// Invalid (NULL) when the agent has never registered with a queued
	// backlog — ClaimNext treats that as "no floor" (deliver everything).
	// Stamped by the register handler via SetBacklogEpoch; see #204.
	BacklogEpoch sql.NullInt64
	// AttentionState is the chamber → operator attention signal (#224).
	// Three values: "idle" (default; no operator action pending), "busy"
	// (chamber mid-tool-call; informational), "awaiting_operator" (chamber
	// has presented a choice/question and needs the operator to weigh in).
	// Set explicitly by chambers via flag_operator / clear_operator_flag.
	// Auto-cleared to "idle" on the chamber's next register call.
	AttentionState string
	// StuckReason is the #291 mailman park signal. Empty (default) = the
	// mailman is delivering normally. A non-empty value (currently only
	// StuckReasonPaneNotFound) means the mailman hit N consecutive
	// pane-probe failures against a persistent failure and parked itself:
	// it stops probing tmux for this agent entirely so a stale / wrong-server
	// pane registration cannot drive the retry storm that wedged the tmux
	// server (2026-06-10 17:54). Messages stay queued; cleared by
	// `register --force`, which resumes delivery on the next mailman loop.
	StuckReason string
	// DisplayName is the chamber-asserted display name (#556) — the
	// case-/space-preserved label ("Lookout", "Master Bosun") set via
	// set_pane_name and shown in agents listings + status outputs. Distinct
	// from Name (the canonical routing key): render-only, never a routing key.
	// Empty when the agent never asserted one (the UI falls back to Name).
	DisplayName string
	// SessionID is the intrinsic Claude session identity (#626 Phase 1b):
	// CLAUDE_CODE_SESSION_ID (a UUID), self-discovered from the registering
	// pane's process tree. When non-empty it is the PRIMARY, exact match key
	// for resolving which pane currently hosts this agent's session (vs the
	// fuzzy `claude --resume <name>` argv match). Empty = legacy / not-yet-
	// discovered -> the resolver falls back to the name-based discover path
	// and logs a deprecation notice (AC6). Set via SetSessionID at register.
	SessionID string
	// Metabolism is the chamber's self-reported context-throughput state (#621):
	// "" (no self-report; default) | warming | saturating | compact-pending. The
	// layer-3 self-report axis — orthogonal to AttentionState (operator-flag,
	// #224) and the auto-observed agent_state probe (#448). Set ONLY by the
	// chamber itself via set_metabolism (a third-party write would clobber the
	// target's real signal — #621 AC#2). Advisory: never gates delivery.
	Metabolism string
	// MetabolismSetAt is the RFC3339/SQLite-format stamp of when Metabolism was
	// last set, so a consumer can discount a stale self-report. Empty when
	// Metabolism is empty (the cleared state stores NULL). Mirrors how
	// observed_state_at stamps the auto-observed axis (#448).
	MetabolismSetAt string
	// RespawnAfterShrinks is the #285 per-chamber respawn threshold N: the
	// mailman respawns the chamber's process after N counted context-shrink
	// events. 0 (the default) = DISABLED — opt-in per chamber, so merging the
	// feature never auto-restarts live processes. Set via SetRespawnAfterShrinks.
	// Read at serve-time each delivery cycle from the agent row.
	RespawnAfterShrinks int
	// RespawnShrinkCount is the #285 running counter of counted context-shrink
	// events since the last respawn (bus-delivered clear in PR1). When it reaches
	// RespawnAfterShrinks (and that threshold is > 0) the mailman fires the
	// respawn pathway and resets this to 0. Incremented via
	// IncrementRespawnShrinkCount, cleared via ResetRespawnShrinkCount.
	RespawnShrinkCount int
	// LastSelfCompactAt is the #285 PR2 self-compact signal: the adapter's
	// post-compaction hook stamps it (via the note-compact helper →
	// SetSelfCompactSignal) each time the chamber self-/compacts. sqliteTimeFormat
	// UTC; empty until the first signal. The mailman edge-detects it against
	// SelfCompactCountedAt to count a self-compact toward RespawnShrinkCount —
	// a self-compact is NOT bus-delivered, so it can't be counted on delivery like
	// PR1's clear.
	LastSelfCompactAt string
	// SelfCompactCountedAt is the #285 PR2 mailman-owned watermark: the value of
	// LastSelfCompactAt the last time the mailman counted a self-compact. Advanced
	// atomically with the increment in CountSelfCompactIfNew, so the same signal is
	// never double-counted. sqliteTimeFormat UTC; empty until the first count. The
	// hook NEVER writes this column — the mailman stays the sole writer of the
	// counter family (respawn_shrink_count + this watermark), preserving PR1's
	// single-flight race-freedom.
	SelfCompactCountedAt string
}

// Delivery-mode constants. Constrained string set; see Agent.DeliveryMode
// for semantics. The store does not enforce membership at insert-time;
// callers should validate via ValidDeliveryMode before writing.
const (
	DeliveryModePasteAndEnter = "paste-and-enter"
	DeliveryModeMailboxOnly   = "mailbox-only"
	// DeliveryModeHookContext (#249, ADR-0009): the recipient agent's session
	// reads pending messages via a lifecycle hook (SessionStart /
	// UserPromptSubmit) that injects them as `additionalContext`, instead of the
	// mailman pasting into the pane. Like mailbox-only, the mailman does NOT
	// paste (it short-circuits at startup); UNLIKE mailbox-only, an adapter-side
	// hook-helper (`tmux-tell-claude hook-context`) actively presents the queued
	// messages on the recipient's next turn and marks them delivered. The
	// substrate stays delivery-method-agnostic — "deliver via Claude hooks"
	// lives entirely in the adapter (cmd/tmux-tell-claude), per ADR-0009's (b).
	DeliveryModeHookContext = "hook-context"
)

// ValidDeliveryMode reports whether s is a known delivery-mode value.
// Empty string is treated as INVALID (callers should default to
// DeliveryModePasteAndEnter explicitly rather than relying on zero-
// value behavior to leak into the schema).
func ValidDeliveryMode(s string) bool {
	return s == DeliveryModePasteAndEnter ||
		s == DeliveryModeMailboxOnly ||
		s == DeliveryModeHookContext
}

// Attention-state constants (#224). Constrained string set; the store
// does not enforce membership at insert-time, so callers should validate
// via ValidAttentionState before writing.
const (
	// AttentionStateIdle is the default — chamber reachable; no operator
	// action pending. Set by register auto-clear when a chamber comes
	// back from /compact, restart, or a normal (re)register.
	AttentionStateIdle = "idle"
	// AttentionStateBusy is the informational mid-tool-call signal. Not
	// actively used by any substrate-side mechanism today; reserved for
	// future hook-driven auto-tracking.
	AttentionStateBusy = "busy"
	// AttentionStateAwaitingOperator is the load-bearing case: the
	// chamber has presented a choice or question and is waiting for the
	// operator to weigh in. Set by flag_operator, cleared by
	// clear_operator_flag or by the chamber's next register.
	AttentionStateAwaitingOperator = "awaiting_operator"
)

// ValidAttentionState reports whether s is a known attention-state value.
// Empty string is treated as INVALID (callers should default to
// AttentionStateIdle explicitly rather than relying on zero-value
// behavior to leak into the schema).
func ValidAttentionState(s string) bool {
	return s == AttentionStateIdle ||
		s == AttentionStateBusy ||
		s == AttentionStateAwaitingOperator
}

// Metabolism constants (#621): the chamber's self-reported, intentional
// context-throughput state — the layer-3 self-report that complements the
// auto-observed agent_state (the 7-state pane probe) and the operator-flag
// attention_state (#224). These three are EXACTLY the states the auto-observer
// cannot infer from pane chrome; the overlapping states the body originally
// proposed (warm/at-rest/rate-limited/awaiting-operator) already exist as
// observed states, so they are deliberately NOT re-invented here.
const (
	// MetabolismWarming: just resumed (e.g. post-/compact), not yet at full
	// context throughput. Distinct from observed "idle" — the chamber IS
	// reachable but self-reports it is still spinning up.
	MetabolismWarming = "warming"
	// MetabolismSaturating: context-load is approaching the /compact-need —
	// not yet idle, not yet at-rest-in-compaction. The early-warning the
	// auto-observer can't see (load is internal, not visible in chrome).
	MetabolismSaturating = "saturating"
	// MetabolismCompactPending: intent-to-/compact stated but not yet executed
	// — the stall seam between "I'll /compact" and the reset firing. Auto-clears
	// once the mailman OBSERVES the chamber actually at-rest-in-compaction
	// (observed-truth supersedes the now-redundant self-report; see
	// ClearMetabolismIfPending).
	MetabolismCompactPending = "compact-pending"
)

// MetabolismEmoji is the single-source legend (#621 AC#4) mapping each
// self-reported metabolism state to its bus-convention emoji (composes with
// #620's emoji conventions). One map so a swap touches exactly one place; the
// reference.md legend points here as canonical rather than re-deriving it.
// Ratified Bosun-side 2026-06-30 (operator may override the glyphs).
var MetabolismEmoji = map[string]string{
	MetabolismWarming:        "🌱",
	MetabolismSaturating:     "🌡️",
	MetabolismCompactPending: "💤",
}

// ValidMetabolism reports whether s is a settable metabolism value. UNLIKE
// ValidAttentionState, the empty string is VALID here: "" is the no-self-report
// default AND the explicit-clear value passed to set_metabolism (a chamber
// retracting its self-report). The three non-empty values are the #621
// vocabulary.
func ValidMetabolism(s string) bool {
	return s == "" ||
		s == MetabolismWarming ||
		s == MetabolismSaturating ||
		s == MetabolismCompactPending
}

// Stuck-reason constants (#291). The empty string is the healthy default
// (mailman delivering normally) and is not enumerated here. A non-empty
// reason parks the mailman: it stops probing tmux for the agent until the
// state is cleared via `register --force`.
const (
	// StuckReasonPaneNotFound is set after N consecutive pane-probe
	// failures of the `can't find pane` shape — a persistent failure
	// (stale registration, wrong tmux server) that, left un-parked,
	// drives the pre-paste-safety-abort retry storm (#291).
	StuckReasonPaneNotFound = "pane-not-found"
)

// ReservedRoutingName reports whether name is reserved as a routing
// primitive (a virtual recipient resolved at delivery-prep time) and
// therefore cannot be registered as a real agent name or alias.
//
// Routing primitives:
//   - "operator" (#228) — resolves to the chamber the operator is
//     currently or was most-recently attached to. A real chamber
//     registering as "operator" would shadow the resolver: the
//     send-side substitution would match against the literal name
//     before ever calling into resolveOperatorTarget, and a phantom
//     chamber could harvest operator-directed traffic.
//
// Other reserved names like "operator-attention" (#224) are NOT in
// this list because they ARE real recipients (the operator pre-
// registers them as mailbox-only agents); they are reserved by
// convention, not by substrate enforcement.
//
// ErrReservedRoutingName is the error returned when a reserved name
// is attempted; callers should surface it to the operator as a clear
// "pick a different name" signal.
func ReservedRoutingName(name string) bool {
	return name == "operator"
}
